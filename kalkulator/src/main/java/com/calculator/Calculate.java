package com.calculator;

public class Calculate {
    public short add(short a, short b) {
        return (short)(a + b);
    }
    
    public short subtract(short a, short b) {
        return (short)(a - b);
    }
    
    public short multiply(short a, short b) {
        return (short)(a * b);
    }
    
    public float divide(short a, short b) {
        return (float)a / b;
    }
}
